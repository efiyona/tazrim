    <h3 style="text-align: center;">תקנון תנאי שימוש ומדיניות פרטיות – מערכת תזרים</h3>
    <p>ברוכים הבאים למערכת <strong>תזרים</strong>. השימוש במערכת, הרשמה אליה והזנת נתונים מהווים את הסכמתך לתנאים המפורטים להלן. אנא קרא/י אותם בקפידה.</p>

    <h4>1. כללי</h4>
    <ul>
        <li>מערכת זו נועדה לסייע בניהול ומעקב אחר הוצאות ותקציב אישי/עסקי.</li>
        <li>השימוש במערכת מוצע כמות שהוא ("AS-IS"). אנו עושים מאמצים רבים להבטיח זמינות ודיוק, אך איננו מתחייבים לחסינות מוחלטת מפני תקלות, נפילות שרת או שיבושים.</li>
    </ul>

    <h4>2. פרטיות ואבטחת מידע</h4>
    <ul>
        <li>אנו לוקחים את פרטיותך ברצינות. נתונים פיננסיים רגישים (כגון סכומי הוצאות, קטגוריות ופרטים מזהים מסוימים) נשמרים במסד הנתונים בצורה מוצפנת ומאובטחת.</li>
        <li>המערכת אוספת ושומרת את הנתונים המוזנים אליה אך ורק לצורך מתן השירות (הצגת התזרים, חישובים והפקת דוחות עבורך).</li>
        <li>אנו מתחייבים לא להעביר, למכור או לשתף את המידע האישי או הפיננסי שלך עם צדדים שלישיים, למעט מקרים בהם נהיה מחויבים לכך על פי חוק.</li>
    </ul>

    <h4>3. שימוש ב-API וקיצורי דרך (Shortcuts)</h4>
    <ul>
        <li>המערכת מאפשרת הנפקת אסימון גישה אישי (API Token) המאפשר הזנת נתונים מרחוק (למשל, דרך קיצורי דרך בנייד או אוטומציות).</li>
        <li><strong>אחריות המשתמש:</strong> ה-API Token משמש כסיסמה לכל דבר. האחריות על שמירת סודיות הטוקן חלה עליך בלבד. כל פעולה שתתבצע באמצעות הטוקן שלך תיחשב כאילו בוצעה על ידך.</li>
        <li>במידה ועולה חשש כי הטוקן שלך נחשף, עליך לייצר טוקן חדש דרך ממשק המערכת באופן מיידי.</li>
    </ul>

    <h4>4. הגבלת אחריות</h4>
    <ul>
        <li>המערכת אינה מהווה תחליף לייעוץ פיננסי, כלכלי או ראיית חשבון מקצועי. כל החלטה כלכלית שתתקבל על בסיס הנתונים המוצגים במערכת היא באחריותך הבלעדית.</li>
        <li>מפתח המערכת לא יישא באחריות לכל נזק ישיר או עקיף, אובדן מידע, או הפסד כספי שייגרם כתוצאה משימוש במערכת, הסתמכות על הנתונים בה, או חוסר יכולת להשתמש בה.</li>
    </ul>

    <h4>5. שימוש הוגן (Fair Use)</h4>
    <ul>
        <li>המשתמש מתחייב לא לבצע כל פעולה שעלולה לפגוע בפעילות התקינה של המערכת, לרבות ניסיונות פריצה, סריקת נתונים אוטומטית (Scraping), או יצירת עומס חריג על שרתי המערכת (Abuse of API).</li>
        <li>אנו שומרים את הזכות להשעות או לחסום משתמשים שיפרו סעיף זה, ללא הודעה מוקדמת.</li>
    </ul>

    <h4>6. שינויים בתקנון</h4>
    <ul>
        <li>אנו רשאים לעדכן את תנאי השימוש ומדיניות הפרטיות מעת לעת. במקרה של שינויים מהותיים, נדאג לעדכן אותך דרך המערכת או נדרוש אישור מחדש של התקנון המעודכן עם כניסתך לחשבון.</li>
    </ul>
    
